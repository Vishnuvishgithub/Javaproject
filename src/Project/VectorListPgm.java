package Project;


		
		
		import java.util.*;

		public class VectorListPgm {
		    public static void main(String[] args) {
		        // Creating a Vector of Strings
		        Vector<String> vector = new Vector<String>();

		        // Adding elements to the Vector
		        vector.add("Apple");
		        vector.add("Banana");
		        vector.add("Cherry");

		        // Displaying the elements of the Vector
		        for (String element : vector)
		        {
		            System.out.println(element);
		        }
		    }
		}

		
		
		/* We create a Vector object called vector that stores strings. 
		We add three elements to the vector using the add method. 
		Finally, we iterate over the elements of the vector using a for-each loop
		and print them to the console. */


