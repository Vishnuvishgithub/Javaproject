package Project;


public class Arrayaddition {

	public static void main(String[] args) {
	
	   int	arr1 [][]= { {10,20},{30,40}};
	   int   arr2 [][] = { {50,20},{50,40}};
	 
	 for (int i=0;i<=2;i++)
	 {
		 for (int j=0;j<=2;j++)
		 {
			 System.out.print(arr1[i][j] + arr2[i][j] + " ");
		 }
	System.out.println();
	 }
	
	
	
	
	
	}

}
